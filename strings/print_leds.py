def print_leds(num):
    leds = [
        ['   #', '####', '####', '#  #', '####', '####', '####', '####', '####', '####'], 
        ['   #', '   #', '   #', '#  #', '#   ', '#  ', '    #', '#  #', '#  #', '#  #'], 
        ['   #', '####', '####', '####', '####', '####', '   #', '####', '####', '#  #'], 
        ['   #', '#  ', '    #', '   #', '   #', '#  #', '   #', '#  #', '   #', '#  #'], 
        ['   #', '####', '####', '   #', '####', '####', '   #', '####', '####', '####'],
    ]

    for row in range(5):
        for digit in str(num):
            try:
                digit = int(digit) - 1
                print(leds[row][digit], end=" ")
            except:
                continue # skip non number
        print()

if __name__ == "__main__":
    print_leds(1234)
